#include "winsock_env.h"

#include <winsock2.h>

namespace lib
{

	////////////////////////////////////////////////////////////////////////////////
	//
	////////////////////////////////////////////////////////////////////////////////
	WinsockEnv::WinsockEnv()
	{
		_wsa_data = new WSADATA();

		WSAStartup(MAKEWORD(2, 2), (LPWSADATA)_wsa_data);
	}

	////////////////////////////////////////////////////////////////////////////////
	//
	////////////////////////////////////////////////////////////////////////////////
	WinsockEnv::~WinsockEnv()
	{
		WSACleanup();

		delete (LPWSADATA)_wsa_data;
	}
}